class BitwiseOperator{
    public static void main(String[]args){
        System.out.println("Hello World");


        // bitwise and 
        // both values should be true then output will be true
        System.out.println(4 & 5);

        // bitwise or
        // one of them should be true , to return true
        System.out.println(5 | 4);

        // xor (^)
        // if values are opposite it will return true  
        // true , false =>>>>>> true



        // shift operators

        // left shift 
        System.out.println(4<<2);  // ----- shift two zeros left shift will always add zeros at end

        // right shift
        System.out.println(4>>2); // ------- right shift will remove last bits because we are shifting by 2


        // 





        

    }
}